package ex3

import "fmt"

func fibonacci(c, quit chan int) {
	x, y := 1, 1
	for {
		select {
		case c <- x:
			x, y = y, x+y
		case <-quit:
			fmt.Println("quit")
			return
		}
	}
}

func Ex3() {
	c := make(chan int)
	quit := make(chan int)
	go func() {
		for i := 0; i < 10; i++ {
			fmt.Println(<-c)
		}
		// 上邊輸出完畢後，給 quit 賦值， 則 fibonacci 函數退出
		quit <- 0
	}()
	fibonacci(c, quit)
}
